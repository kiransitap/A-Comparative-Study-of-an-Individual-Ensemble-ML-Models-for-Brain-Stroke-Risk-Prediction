import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

data = pd.read_csv('stroke_risk_dataset_v2.csv')

print("Dataset shape:", data.shape)

print("Columns in dataset:", data.columns.tolist())

display(data.head())

"""Exploratory Data Analysis (EDA)"""

# Data information and missing values
print("\nData Info:")
print(data.info())
print("\nMissing Values:")
print(data.isnull().sum())

# Summary statistics for numerical features
print("\nSummary Statistics:")
print(data.describe())

# Define target variable
target = 'at_risk'
if target not in data.columns:
    raise ValueError(f"Target column '{target}' not found. Available columns: {data.columns.tolist()}")

# Plot target distribution
plt.figure(figsize=(6,4))
sns.countplot(x=target, data=data)
plt.title("Distribution of 'at_risk'")
plt.xlabel("At Risk (1=yes, 0=no)")
plt.ylabel("Count")
plt.show()

# List numeric features (excluding target and stroke_risk_percentage)
numeric_features = data.select_dtypes(include=['int64', 'float64']).columns.tolist()
for col in [target, 'stroke_risk_percentage']:
    if col in numeric_features:
        numeric_features.remove(col)

# Histograms for numeric features
data[numeric_features].hist(bins=20, figsize=(15,10))
plt.suptitle("Distribution Plots of Numeric Features")
plt.tight_layout()
plt.show()

# Correlation heatmap for numeric features (excluding stroke_risk_percentage)
plt.figure(figsize=(10,8))
sns.heatmap(data[numeric_features].corr(), annot=True, cmap='coolwarm', fmt=".2f")
plt.title("Correlation Heatmap (excluding stroke_risk_percentage)")
plt.show()

"""Data Preprocessing"""

# Identify categorical features
categorical_features = data.select_dtypes(include=['object']).columns.tolist()
print("\nCategorical Features:", categorical_features)

# Fill missing values for numeric and categorical features
for col in data.columns:
    if data[col].dtype == 'object':
        data[col] = data[col].fillna(data[col].mode()[0])
    else:
        data[col] = data[col].fillna(data[col].median())

from sklearn.compose import ColumnTransformer
from sklearn.preprocessing import StandardScaler, OneHotEncoder

# Drop the target and stroke_risk_percentage from the features
#features_to_drop = [target, 'stroke_risk_percentage']
#X = data.drop(columns=features_to_drop)
#y = data[target]  replace with

# Drop target, derived feature, and near-diagnostic leakage features
leakage_features = [
    'anxiety_doom',
    'irregular_heartbeat',
    'excessive_sweating'
]

features_to_drop = [target, 'stroke_risk_percentage'] + leakage_features

X = data.drop(columns=features_to_drop)
y = data[target]


# Update numeric features for preprocessing based on X
numeric_features = X.select_dtypes(include=['int64', 'float64']).columns.tolist()

# Create preprocessor: scale numeric features and one-hot encode categorical features
preprocessor = ColumnTransformer(
    transformers=[
        ('num', StandardScaler(), numeric_features),
        ('cat', OneHotEncoder(drop='first'), categorical_features)
    ])

"""Train-Test Split"""

from sklearn.model_selection import train_test_split

# Split the data into training and testing sets
X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.2, stratify=y, random_state=42)
print("Train set shape:", X_train.shape)
print("Test set shape:", X_test.shape)

# Classifiers
from sklearn.linear_model import LogisticRegression
from sklearn.tree import DecisionTreeClassifier
from sklearn.ensemble import RandomForestClassifier, GradientBoostingClassifier

from sklearn.pipeline import Pipeline


# Set random state for reproducibility
RANDOM_STATE = 42

# Define models
models = {
    'Logistic Regression': LogisticRegression(
    random_state=RANDOM_STATE,
    max_iter=1000,
    penalty='l2',
    C=0.05,              # stronger regularization
    solver='liblinear'
    ),

    'Decision Tree': DecisionTreeClassifier(
        random_state=RANDOM_STATE,
        max_depth=6,               # limit complexity
        min_samples_split=20,
        min_samples_leaf=10
    ),

    'Random Forest': RandomForestClassifier(
        random_state=RANDOM_STATE,
        n_estimators=80,
        max_depth=7,
        min_samples_split=20,
        min_samples_leaf=10
    ),

    'Gradient Boosting': GradientBoostingClassifier(
        random_state=RANDOM_STATE,
        n_estimators=80,
        learning_rate=0.05,
        max_depth=3
    )
}

# Create pipelines for each model
pipelines = {}
for name, model in models.items():
    pipelines[name] = Pipeline(steps=[
        ('preprocessor', preprocessor),
        ('classifier', model)
    ])

from sklearn.model_selection import cross_val_score
from sklearn.metrics import (accuracy_score, precision_score, recall_score, f1_score, roc_auc_score, classification_report, confusion_matrix)

results = {}
cv_results = {}

for name, pipeline in pipelines.items():
    print(f"\n--- Training {name} ---")

    # 5-fold cross-validation using ROC AUC
    cv_scores = cross_val_score(pipeline, X_train, y_train, cv=5, scoring='roc_auc')
    cv_results[name] = cv_scores
    print(f"{name} Cross-Validation ROC AUC: Mean = {np.mean(cv_scores):.4f}, Std = {np.std(cv_scores):.4f}")


# Fit the pipeline on the full training set and predict on the test set
    pipeline.fit(X_train, y_train)
    y_pred = pipeline.predict(X_test)
    y_proba = pipeline.predict_proba(X_test)[:, 1]

    # Compute evaluation metrics
    acc = accuracy_score(y_test, y_pred)
    prec = precision_score(y_test, y_pred, zero_division=0)
    rec = recall_score(y_test, y_pred, zero_division=0)
    f1 = f1_score(y_test, y_pred, zero_division=0)
    roc_auc = roc_auc_score(y_test, y_proba)

# Generate confusion matrix and classification report
    conf_matrix = confusion_matrix(y_test, y_pred)
    report = classification_report(y_test, y_pred, zero_division=0)

    results[name] = {
        'accuracy': acc,
        'precision': prec,
        'recall': rec,
        'f1': f1,
        'roc_auc': roc_auc,
        'confusion_matrix': conf_matrix,
        'report': report
    }

    print(f"{name} Test Accuracy: {acc:.4f}")
    print(f"{name} Test Precision: {prec:.4f}")
    print(f"{name} Test Recall: {rec:.4f}")
    print(f"{name} Test F1 Score: {f1:.4f}")
    print(f"{name} Test ROC AUC: {roc_auc:.4f}")
    print("\nConfusion Matrix:")
    print(conf_matrix)
    print("\nClassification Report:")
    print(report)

# Plot confusion matrix with labels for interpretation
    plt.figure(figsize=(6,4))
    sns.heatmap(conf_matrix, annot=True, fmt="d", cmap="Blues", cbar=False)
    plt.title(f"{name} - Confusion Matrix")
    plt.xlabel("Predicted Label\n(1 = Predicted At Risk, 0 = Predicted Not At Risk)")
    plt.ylabel("True Label\n(1 = Actual At Risk, 0 = Actual Not At Risk)")
    plt.show()

# ===========================
# Ensemble: Voting Classifier
# ===========================
from sklearn.ensemble import VotingClassifier

# Create a soft voting ensemble using the existing pipelines
# (each pipeline already has: preprocessor + classifier)
#voting_estimators = [(name, pipe) for name, pipe in pipelines.items()] replace this

voting_estimators = [
    ('Logistic Regression', pipelines['Logistic Regression']),
    ('Decision Tree', pipelines['Decision Tree']),
    ('Random Forest', pipelines['Random Forest']),
    ('Gradient Boosting', pipelines['Gradient Boosting'])

]


voting_clf = VotingClassifier(
    estimators=voting_estimators,
    voting='soft',        # soft voting uses predicted probabilities
    n_jobs=-1
)

print("\n=== Training Voting Classifier Ensemble ===")

# 5-fold cross-validation using ROC AUC on training set
voting_cv_scores = cross_val_score(voting_clf, X_train, y_train, cv=5, scoring='roc_auc')
cv_results['Voting Classifier'] = voting_cv_scores
print(f"Voting Classifier Cross-Validation ROC AUC: Mean = {np.mean(voting_cv_scores):.4f}, "
      f"Std = {np.std(voting_cv_scores):.4f}")

# Fit ensemble on full training data
voting_clf.fit(X_train, y_train)

# Predict on test set
y_pred_v = voting_clf.predict(X_test)
y_proba_v = voting_clf.predict_proba(X_test)[:, 1]

# Compute evaluation metrics
acc_v = accuracy_score(y_test, y_pred_v)
prec_v = precision_score(y_test, y_pred_v, zero_division=0)
rec_v = recall_score(y_test, y_pred_v, zero_division=0)
f1_v = f1_score(y_test, y_pred_v, zero_division=0)
roc_auc_v = roc_auc_score(y_test, y_proba_v)

# Confusion matrix and classification report
conf_matrix_v = confusion_matrix(y_test, y_pred_v)
report_v = classification_report(y_test, y_pred_v, zero_division=0)

results['Voting Classifier'] = {
    'accuracy': acc_v,
    'precision': prec_v,
    'recall': rec_v,
    'f1': f1_v,
    'roc_auc': roc_auc_v,
    'confusion_matrix': conf_matrix_v,
    'report': report_v
}

print(f"\nVoting Classifier Test Accuracy: {acc_v:.4f}")
print(f"Voting Classifier Test Precision: {prec_v:.4f}")
print(f"Voting Classifier Test Recall: {rec_v:.4f}")
print(f"Voting Classifier Test F1 Score: {f1_v:.4f}")
print(f"Voting Classifier Test ROC AUC: {roc_auc_v:.4f}")
print("\nConfusion Matrix:")
print(conf_matrix_v)
print("\nClassification Report:")
print(report_v)

# Plot confusion matrix for the ensemble
plt.figure(figsize=(6,4))
sns.heatmap(conf_matrix_v, annot=True, fmt="d", cmap="Blues", cbar=False)
plt.title("Voting Classifier - Confusion Matrix")
plt.xlabel("Predicted Label\n(1 = Predicted At Risk, 0 = Predicted Not At Risk)")
plt.ylabel("True Label\n(1 = Actual At Risk, 0 = Actual Not At Risk)")
plt.show()

# Create summary DataFrame of model performance
summary = pd.DataFrame({
    'Model': list(results.keys()),
    'Test Accuracy': [results[m]['accuracy'] for m in results],
    'Test Precision': [results[m]['precision'] for m in results],
    'Test Recall': [results[m]['recall'] for m in results],
    'Test F1 Score': [results[m]['f1'] for m in results],
    'Test ROC AUC': [results[m]['roc_auc'] for m in results],
    'CV ROC AUC Mean': [np.mean(cv_results[m]) for m in cv_results],
    'CV ROC AUC Std': [np.std(cv_results[m]) for m in cv_results]
})

print("\nSummary of Model Performance:")
display(summary.sort_values(by='Test ROC AUC', ascending=False))

# Visualize Test ROC AUC scores for comparison
plt.figure(figsize=(10,6))
sns.barplot(x='Model', y='Test ROC AUC', data=summary, palette='pastel')
plt.xticks(rotation=45)
plt.title("Test ROC AUC Score by Model")
plt.ylabel("ROC AUC Score")
plt.show()

